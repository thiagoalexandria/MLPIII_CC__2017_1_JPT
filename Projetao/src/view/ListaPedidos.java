    public void lerjTable(){
        
        DefaultTableModel mc = (DefaultTableModel) jTcarro.getModel();
        SalvaBanco mostrac = new SalvaBanco();
        
        for(RegistroPedidos p: mostrac.listarpc()){
            
            mc.addRow(new Object[]{
            p.getId(),
            p.getMarcarro(),
            p.getModelocarro(),
            p.getCodcarro()
            });
            
        }}
        
     public void lerjTablem(){
        
        DefaultTableModel mm = (DefaultTableModel) jTmoto.getModel();
        SalvaBanco mostram = new SalvaBanco();
        
        for(RegistroPedidosm p: mostram.listarpm()){
            
            mm.addRow(new Object[]{
            p.getId(),
            p.getMarmoto(),
            p.getModelomoto(),
            p.getCodmoto()
            });
            
        }}

